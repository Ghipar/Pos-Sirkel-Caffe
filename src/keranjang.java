    public void tgl() {
        Calendar kal = new GregorianCalendar();
        int tahun = kal.get(Calendar.YEAR);
        int bulan = kal.get(Calendar.MONTH) + 1;
        int tgl = kal.get(Calendar.DAY_OF_MONTH);
        String tanggal = tahun + "-" + bulan + "-" + tgl;
        uwuu.tgle.setText(tanggal);
    }

